package selection.java;

public class SwitchCase {

	public static void main(String[] args) {
		byte option = 4;
		switch(option) {
		case 1:
			System.out.println("print1");
			break;
		case 2:
			System.out.println("print2");
			break;
		case 3:
			System.out.println("print3");
			break;
		default:
			System.out.println("default printed");
			
		}
	}

}

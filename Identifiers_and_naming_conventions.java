package Corejavaelements;                           

public class Identifiers_and_naming_conventions {   //classname should start with caps

	public static void main(String[] args) {       // methodname must starts with smallletter and in verb
		int element1 = 10;                        //  element1 is an identifier 
		int element2 = 20;
		int identifier=100;                      // variable should be in lowercase
		int sum = element1+element2;               
		System.out.println("sum is" +sum);
		System.out.println(identifier);          //class can be used as identifier if not defined in the code
		
	}

}
